package com.chris.fin_shark.common.enums;

import lombok.Getter;

/**
 * Job 類型列舉
 *
 * 遵守總綱 4.5.3 Job 類型定義規範
 *
 * @author chris
 * @since 2025-12-24
 */
@Getter
public enum     JobType {

    // ========================================================================
    // 資料同步類型 (DATA_SYNC) - 已完整定義
    // ========================================================================

    /**
     * 同步股價資料
     * 執行頻率: 每日 15:00
     */
    DATA_SYNC("DATA_SYNC", "資料同步"),

    // ========================================================================
    // 計算類型 (CALCULATION) - 已完整定義
    // ========================================================================

    /**
     * 計算技術指標
     * 執行頻率: 每日 16:00
     */
    CALCULATION("CALCULATION", "指標計算"),

    // ========================================================================
    // 信號偵測類型 (SIGNAL_DETECTION) - 已完整定義
    // ========================================================================

    /**
     * 偵測技術面信號
     * 執行頻率: 每日 17:00
     */
    SIGNAL_DETECTION("SIGNAL_DETECTION", "信號偵測"),

    // ========================================================================
    // 通知類型 (NOTIFICATION) - 已完整定義
    // ========================================================================

    /**
     * 發送通知
     * 執行頻率: 即時或每日
     */
    NOTIFICATION("NOTIFICATION", "通知發送"),

    // ========================================================================
    // 維護類型 (MAINTENANCE) - 已完整定義
    // ========================================================================

    /**
     * 系統維護
     * 執行頻率: 每日或每週
     */
    MAINTENANCE("MAINTENANCE", "系統維護"),

    // ========================================================================
    // TODO: 其他類型可在實作時補充
    // 例如:
    // - REPORT: 報表生成
    // - BACKUP: 資料備份
    // - ANALYSIS: 資料分析
    // ========================================================================

    ;

    /**
     * 類型代碼
     */
    private final String code;

    /**
     * 類型描述
     */
    private final String description;

    /**
     * 建構子
     */
    JobType(String code, String description) {
        this.code = code;
        this.description = description;
    }
}
